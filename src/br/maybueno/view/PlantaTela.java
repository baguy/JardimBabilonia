    // BORBOLETA -- SALVAR
    private void lSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lSalvarMouseClicked
        
        String nome = this.txtNome.getText();
        String tamanho = this.txtTamanho.getText();
        String status = this.comboxStatus.getSelectedItem().toString();
        Object jardim = this.comboxJardim.getSelectedItem();
        
        this.controller.cadastrarPlanta(nome, tamanho, status, (JardimModel) jardim);
        
    }//GEN-LAST:event_lSalvarMouseClicked

    // BORBOLETA -- ATUALIZAR
    private void lAtualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lAtualizarMouseClicked
        
        String nome, status, tamanho; int idP;
            nome = this.txtAlterarNome.getText();
            tamanho = this.txtAlterarTamanho.getText();
            status = this.txtAlterarStatus.getText();
            idP = Integer.parseInt(this.txtIdPlanta.getText());
            Object idJardimbox = this.comboxAlterarIdJardim.getSelectedItem();   
          
        this.controller.atualizarPlanta(nome, tamanho, status, (JardimModel) idJardimbox, idP);
        
    }//GEN-LAST:event_lAtualizarMouseClicked

